#include <iostream>
using namespace std;
int main()
{
    int X, Y;
    cin >> X >> Y;
    cout << X << " + " << Y << " = " << X + Y << endl;
    cout << X << " * " << Y << " = " << X * Y << endl;
    cout << X << " - " << Y << " = " << X - Y << endl;

    return 0;
}